package calculatore;

public class Operaciones {

	
	
	
	
	//Metodos operadores
	
	public int suma (int num1, int num2) {
		int resultado;
		resultado = num1 + num2;
		return resultado;
	}
	
	public int resta(int num1, int num2) {
		int resultado;
		resultado = num1 - num2;
		return resultado;	
	}
	public int multi(int num1, int num2) {
		int resultado;
		resultado = num1 * num2;
		return resultado;	
	}
	public int divi(int num1, int num2) {
		int resultado;
		resultado = num1 / num2;
		return resultado;	
	}
}
